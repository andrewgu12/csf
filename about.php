					<p>The California Scholarship Federation (CSF-CJSF) is a non-profit service organization, founded in 1921 by Mr. Charles F. Seymour who sought to honor and unify outstanding high school students in the state of California. Today, approximately 1,000 CSF chapters and 600 CJSF chapters foster the recognition, motivation and education of academically talented students, fostering in their chapter memberships the importance of community service driven by the youth. Further details about our umbrella organization are found <a href="http://csf-cjsf.org/">here.</a>
Established in 1970, Monta Vista California Scholarship Federation is the 900th chapter of CSF, the high school division of CSF-CJSF. We emphasize high standards of scholarship and community service for high school students all over California. Since its founding MVCSF remains Monta Vista's premiere honorary service organization with over 350 active members. In the Class of 2009, MVCSF graduated 43 100% Life Members and 49 Life Members, denoting the CSF volunteers who have served their high school careers. Our chapter follows the motto "Scholarship for Service," as our members are foremost dedicated to the betterment of the community while striving for academic excellence. </p>
					<p><b>How do I Join?</b> <br />
					New members to Monta Vista CSF must apply during the two-week membership drive at the beginning of each school semester. Freshmen at Monta Vista may apply for CSF membership only during the second semester membership drive. New and veteran CSF members must reapply for membership every semester. To qualify for membership, you must earn a minimum of 10 "CSF points" from at least 5 certified courses. At least 7 Points must be earned from List I or List II courses, and the remaining 3 points may be from List III courses. Physical Education (PE) and Teacher Assistant (TA) grades may not be counted. <br /></p>
					<p><b>Volunteer with Us</b><br />
					As a service club, group events such as playground cleanup, festival running, soup kitchens and tree planting are best suited for our volunteers, but we can accommodate almost any volunteer need on a case-by-case basis. However, we limit event location boundaries to within the South Bay Area (Cupertino, Saratoga, Sunnyvale, Palo Alto, Menlo) out of respect for our volunteers.
If your organization is looking for help at an upcoming volunteer event, please leave us your contact person's name, primary phone number and email address here. We look forward to hearing from you!
Monta Vista CSF's strives to promote community activism at the Monta Vista campus, connecting students with a variety of volunteer opportunities around the Bay Area. Some of our partnerships include <a href="http://www.savesfbay.org/">Save the Bay</a>, <a href="http://www.sacredheartcs.org/">Sacred Heart</a>, <a href="http://www.cityofpaloalto.org/gov/depts/csd/artcenter/default.asp">the Palo Alto Art Center</a>, <a href="http://www.raft.net/">RAFT</a>, <a href="http://apscuhuru.org/uhurufoods/">Uhuru Foods</a> and the <a href="http://www.cupertino.org/">City of Cupertino.</a>
