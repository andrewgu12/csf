<h1>Class Lists</h1>
<p>Membership into CSF is based on the grades you received last semester. </p>
<ol>
	<li>You must earn at least <b>10 CSF points</b> to qualify.</li>
		<ul>
			<li>In regular courses, an <b>A is 3 points</b>, a <b>B is 1 point</b>, and a <b>C is 0 points</b></li>
			<li>One additional CSF point shall be granted for a grade of A or B in an AP or Honors course; up to a maximum of <b>two</b> such points each semester.</li>
			<li>A grade of <b>D or F</b> disqualifies you from membership this semester.</li>
		</ul>
	<li>Points can be taken only from classes on the CSF Course Lists (see below)	</li>
		<ul>
			<li>At least 7 points must be earned from Lists I & II. 4 of these 7 must come from List I only.</li>
			<li>The remaining points can come from any List.</li>
		</ul>
	<li>You make your points from <em><u>no more than 5</u></em> classes. </li>
</ol>
